  def reason_code(
        r_alert(description: description, role: role) = alert,
        role,
        protocolName,
        stateName
      ) do
    {prefixFmt, prefixArgs} = alert_prefix_format(protocolName, role, stateName)
    {fmt, args} = own_alert_format_depth(alert)

    txt =
      :lists.flatten(
        :io_lib.format(
          prefixFmt ++ fmt,
          prefixArgs ++ args
        )
      )

    {:tls_alert, {description_atom(description), txt}}
  end

  def reason_code(r_alert(description: description) = alert, role, protocolName, stateName) do
    {fmt, args} = alert_format(protocolName, role, stateName, alert)
    txt = :lists.flatten(:io_lib.format(fmt, args))
    {:tls_alert, {description_atom(description), txt}}
  end

  def own_alert_format(protocolName, role, stateName, alert) do
    {prfixFmt, prefixArgs} = alert_prefix_format(protocolName, role, stateName)
    {fmt, args} = own_alert_format(alert)
    {prfixFmt ++ fmt, prefixArgs ++ args}
  end

  def alert_format(protocolName, role, stateName, alert) do
    {prfixFmt, prefixArgs} = alert_prefix_format(protocolName, role, stateName)
    {fmt, args} = alert_format(alert)
    {prfixFmt ++ fmt, prefixArgs ++ args}
  end

  defp alert_prefix_format(protocolName, role, stateName) do
    {'~s ~p: In state ~p', [protocolName, role, stateName]}
  end

  defp own_alert_format(r_alert(reason: reason) = alert) do
    txt = own_alert_txt(alert)

    case reason do
      :undefined ->
        {' ~s\n', [txt]}

      ^reason ->
        {' ~s\n - ~p', [txt, reason]}
    end
  end

  defp own_alert_format_depth(r_alert(reason: reason) = alert) do
    txt = own_alert_txt(alert)

    case reason do
      :undefined ->
        {' ~s\n', [txt]}

      ^reason ->
        {' ~s\n ~P', [txt, reason, 20]}
    end
  end

  defp own_alert_txt(
         r_alert(
           level: level,
           description: description,
           where: %{:line => line, :file => mod},
           role: role
         )
       ) do
    'at ' ++
      mod ++
      ':' ++
      :erlang.integer_to_list(line) ++
      ' generated ' ++
      :string.uppercase(:erlang.atom_to_list(role)) ++
      ' ALERT: ' ++ level_txt(level) ++ description_txt(description)
  end

  defp alert_format(alert) do
    txt = alert_txt(alert)
    {' ~s\n', [txt]}
  end

  defp alert_txt(r_alert(level: level, description: description, role: role)) do
    'received ' ++
      :string.uppercase(:erlang.atom_to_list(role)) ++
      ' ALERT: ' ++ level_txt(level) ++ description_txt(description)
  end

